"""
Пример за setw от C++.
В C++:
cout << setw(5) << 215;
→ числото се извежда с ширина 5, подравнено надясно.

В Python това става така:
"""
print(f"{215:>5}")    # → '  215'

print(f"{430:>6}")    # → '   430' (6 позиции)


"""
Пример за setprecision от C++.
cout << setprecision(3) << 3.12345;
→ Извежда 3.12 (с 3 значещи цифри).

В Python използваме:
"""
print(f"{3.12345:.2f}")  # → 3.12 (2 цифри след десетичната точка)

"""
cout << setprecision(2) << 5.6881;
→ Извежда 5.7 (2 значещи цифри — тук се закръглява).

В Python:
"""
print(f"{5.6881:.1f}")  # → 5.7 (1 цифра след десетичната точка)

"""
cout << setprecision(1) << 2.7;
→ Извежда 3 (закръгляване до цяло).

В Python:
"""
print(round(2.7))       # → 3
